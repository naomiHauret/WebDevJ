import java.awt.Color;


public class Principale implements CustomColor{
	///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	static Interface it;
	static Nouveau n;
	///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	public static void main(String []args){
		ouvrirInterface();
		
		
	}
	///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	public static void ouvrirInterface(){
		it= new Interface();
		it.setBoutons(true, true,true, false, false, false, true);
		it.setBackgrounds(CustomColor.LIGHTGREEN, CustomColor.LIGHTGREEN, CustomColor.LIGHTGREEN, CustomColor.LIGHTGREEN);
		it.iniAllVectors();

		it.ajoutOngletHtml("exemple.html");
		it.ajoutOngletCss("exemple.css");
		it.ajoutOngletJs("exemple.js");
		it.setEnabledTextPanes(true,false,false);
		it.charger();
		it.setVisible(true);
	}
	///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	public static void ouvrirNouveau(){
		n=new Nouveau();
		n.initialise_action();
		n.setVisible(true);
	}
	///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	public static void demandeAjoutHtml(String nom){
		it.ajoutOngletHtml(nom);
	}
	///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	public static void demandeAjoutCss(String nom){
		it.ajoutOngletCss(nom);

	}
	///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	public static  void demandeAjoutJs(String nom){
		it.ajoutOngletJs(nom);

	}
	
}
